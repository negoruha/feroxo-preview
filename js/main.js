const getTranslation = (key, lang = currentLanguage) => translations[lang]?.[key] ?? translations.en[key] ?? '';
let currentLanguage = localStorage.getItem('feroxo-language') || 'en';

const applyTranslations = (lang) => {
  currentLanguage = translations[lang] ? lang : 'en';
  localStorage.setItem('feroxo-language', currentLanguage);
  document.documentElement.lang = currentLanguage === 'cz' ? 'cs' : 'en';

  document.querySelectorAll('[data-i18n]').forEach((node) => {
    const text = getTranslation(node.dataset.i18n, currentLanguage);
    if (text) node.textContent = text;
  });

  document.querySelectorAll('[data-i18n-html]').forEach((node) => {
    const html = getTranslation(node.dataset.i18nHtml, currentLanguage);
    if (html) node.innerHTML = html;
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach((node) => {
    const text = getTranslation(node.dataset.i18nPlaceholder, currentLanguage);
    if (text) node.setAttribute('placeholder', text);
  });

  document.querySelectorAll('[data-i18n-aria-label]').forEach((node) => {
    const text = getTranslation(node.dataset.i18nAriaLabel, currentLanguage);
    if (text) node.setAttribute('aria-label', text);
  });

  document.querySelectorAll('[data-lang-option]').forEach((button) => {
    button.classList.toggle('is-active', button.dataset.langOption === currentLanguage);
  });
};

const modal = document.querySelector('[data-modal="request"]');
const modalOpenButtons = document.querySelectorAll('[data-open-request]');
const modalCloseButtons = document.querySelectorAll('[data-close-request]');
const language = document.querySelector('[data-language]');
const languageButton = document.querySelector('[data-language-button]');
const languageOptions = document.querySelectorAll('[data-lang-option]');

const openModal = () => {
  modal?.classList.add('is-open');
  modal?.setAttribute('aria-hidden', 'false');
  document.body.style.overflow = 'hidden';
};

const closeModal = () => {
  modal?.classList.remove('is-open');
  modal?.setAttribute('aria-hidden', 'true');
  document.body.style.overflow = '';
};

modalOpenButtons.forEach((button) => button.addEventListener('click', openModal));
modalCloseButtons.forEach((button) => button.addEventListener('click', closeModal));

window.addEventListener('keydown', (event) => {
  if (event.key === 'Escape') {
    closeModal();
    language?.classList.remove('is-open');
  }
});

languageButton?.addEventListener('click', (event) => {
  event.stopPropagation();
  language?.classList.toggle('is-open');
});

languageOptions.forEach((button) => {
  button.addEventListener('click', (event) => {
    event.preventDefault();
    applyTranslations(button.dataset.langOption);
    language?.classList.remove('is-open');
  });
});

document.addEventListener('click', (event) => {
  if (language && !language.contains(event.target)) language.classList.remove('is-open');
});

