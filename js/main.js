const newsTrack = document.querySelector('[data-news-track]');
const newsPrev = document.querySelector('[data-news-prev]');
const newsNext = document.querySelector('[data-news-next]');
const newsDots = document.querySelector('[data-news-dots]');
let realNewsCards = [...document.querySelectorAll('[data-news-card]')];
let newsCards = [...realNewsCards];
let newsRealIndex = Math.min(1, Math.max(0, realNewsCards.length - 1));

if (newsTrack && realNewsCards.length > 1) {
  const firstClone = realNewsCards[0].cloneNode(true);
  const lastClone = realNewsCards[realNewsCards.length - 1].cloneNode(true);
  firstClone.setAttribute('data-news-clone', 'true');
  lastClone.setAttribute('data-news-clone', 'true');
  firstClone.classList.remove('is-active');
  lastClone.classList.remove('is-active');
  newsTrack.insertBefore(lastClone, newsTrack.firstElementChild);
  newsTrack.appendChild(firstClone);
  newsCards = [...newsTrack.querySelectorAll('[data-news-card]')];
}

const getNewsInternalIndex = () => (realNewsCards.length > 1 ? newsRealIndex + 1 : 0);
const getNewsGap = () => {
  if (!newsTrack) return 0;
  const style = window.getComputedStyle(newsTrack);
  return parseFloat(style.columnGap || style.gap || 0) || 0;
};
const getNewsSideStep = () => {
  const internalIndex = getNewsInternalIndex();
  const sideCard = newsCards.find((card, index) => index !== internalIndex) || newsCards[0];
  if (!sideCard) return 0;
  return sideCard.getBoundingClientRect().width + getNewsGap();
};
const updateNewsDots = () => {
  if (!newsDots) return;
  [...newsDots.querySelectorAll('.latest-news__dot')].forEach((dot, index) => {
    dot.classList.toggle('is-active', index === newsRealIndex);
  });
};
const updateNewsSlider = () => {
  if (!newsTrack || newsCards.length === 0) return;
  const internalIndex = getNewsInternalIndex();
  newsCards.forEach((card, index) => card.classList.toggle('is-active', index === internalIndex));
  requestAnimationFrame(() => {
    const step = getNewsSideStep();
    const isMobile = window.matchMedia('(max-width: 760px)').matches;
    const offset = isMobile ? internalIndex * step : Math.max(0, internalIndex - 1) * step;
    newsTrack.style.transform = `translateX(${-offset}px)`;
    updateNewsDots();
  });
};
const setNewsIndex = (nextIndex) => {
  if (realNewsCards.length === 0) return;
  const maxIndex = realNewsCards.length - 1;
  if (nextIndex < 0) newsRealIndex = maxIndex;
  else if (nextIndex > maxIndex) newsRealIndex = 0;
  else newsRealIndex = nextIndex;
  updateNewsSlider();
};
if (newsDots && realNewsCards.length > 0) {
  realNewsCards.forEach((_, index) => {
    const dot = document.createElement('button');
    dot.className = 'latest-news__dot';
    dot.type = 'button';
    dot.setAttribute('aria-label', `Show news ${index + 1}`);
    dot.addEventListener('click', () => setNewsIndex(index));
    newsDots.appendChild(dot);
  });
}
newsPrev?.addEventListener('click', () => setNewsIndex(newsRealIndex - 1));
newsNext?.addEventListener('click', () => setNewsIndex(newsRealIndex + 1));
window.addEventListener('resize', updateNewsSlider);
updateNewsSlider();

